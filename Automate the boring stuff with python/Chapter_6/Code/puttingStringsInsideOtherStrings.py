# Using + Operator
name = "AI"
age = 4000
print("Hello, my name is " + name + ". I am " + str(age) + " years old.")
# Output: Hello, my name is AI. I am 4000 years old.

# Using the format() method
name = "AI"
age = 4000
print("Hello, my name is {}. I am {} years old.".format(name, age))
# Output: Hello, my name is AI. I am 4000 years old.

# Using f-strings
name = "AI"
age = 4000
print(f"Hello, my name is {name}. I am {age} years old.")
# Output: Hello, my name is AI. I am 4000 years old.

# Using the % operator
name = "AI"
age = 4000
print("Hello, my name is %s. I am %s years old." % (name, age))
# Output: Hello, my name is AI. I am 4000 years old.
